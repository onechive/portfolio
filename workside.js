const params = new URLSearchParams(window.location.search);
const projectId = params.get("id");
const project = allProjects[projectId];
console.log("project:", project);  // 이 줄 추가

if (project) {
  document.getElementById("detail-title").textContent = project.title;
  document.getElementById("detail-subtitle").textContent = project.subtitle;
  document.getElementById("detail-genre").textContent = project.genre;
  document.getElementById("detail-year").textContent = project.year;
  document.getElementById("detail-description").textContent = project.description;

  const imageContainer = document.getElementById("detail-images");
imageContainer.innerHTML = "";

project.images.forEach(url => {
  let element;

  if (url.endsWith(".mp4")) {
    element = document.createElement("video");
    element.src = url;
    element.controls = true;
    element.className = "WorkSide-image";

    

    
  } else if (url.includes("youtube.com") || url.includes("youtu.be")) {
    const embedUrl = convertToEmbed(url);
    element = document.createElement("iframe");
    element.src = embedUrl;
    element.width = "100%";
    element.height = "400";
    element.allow = "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture";
    element.allowFullscreen = true;
    element.frameBorder = "0";  // 꼭 추가
    element.className = "WorkSide-image";




  } else {
    element = document.createElement("img");
    element.src = url;
    element.alt = project.title;
    element.className = "WorkSide-image";
  }

  imageContainer.appendChild(element);
});

} else {
  document.body.innerHTML = "<h1 class='WorkSide-error'>Project not found.</h1>";
}




function convertToEmbed(url) {
  if (url.includes("watch?v=")) {
    return url.replace("watch?v=", "embed/");
  }
  return url;
}












